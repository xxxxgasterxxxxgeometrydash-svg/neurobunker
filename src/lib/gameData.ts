// ─── СИСТЕМНЫЙ ПРОМПТ для AI ─────────────────────────────────
export function buildSystemPrompt(params: {
  playerName: string;
  disaster: Disaster;
  revealedCards: Partial<import('../types/game').PlayerCards>;
  allRevealedCards: string; // текст о других игроках
  phase: import('../types/game').GamePhase;
  round: number;
  isAccused: boolean;
  specialCondition?: string;
}): string {
  const { playerName, disaster, revealedCards, allRevealedCards, phase, round, isAccused, specialCondition } = params;

  const myCards = Object.entries(revealedCards)
    .map(([k, v]) => `• ${CARD_LABELS[k as keyof typeof CARD_LABELS] ?? k}: ${v}`)
    .join('\n');

  const accusedNote = isAccused
    ? '\n⚠️ ВНИМАНИЕ: Тебя выставили на голосование! Это твой последний шанс убедить всех в своей ценности. Говори убедительно и эмоционально!'
    : '';

  const phaseInstruction: Record<import('../types/game').GamePhase, string> = {
    setup: 'Представься кратко.',
    playing: `Раунд ${round}. Открой одну карту и объясни почему ты нужен бункеру.`,
    voting: 'Сейчас голосование. Выскажись кто должен быть изгнан и ПОЧЕМУ. Будь конкретен.',
    defense: 'Защищай себя! У тебя 30 секунд. Используй все открытые карты как аргументы.',
    final: 'Игра окончена. Подведи итог своего участия.',
    survival: 'Начинается История Выживания. Оцени насколько твои навыки помогут против угроз.',
    ended: 'Игра завершена. Скажи финальное слово.',
  };

  return `Ты — участник апокалиптического шоу «НейроБункер».
Твоё имя: ${playerName}
Катастрофа: ${disaster.title} — ${disaster.description}

Твои раскрытые характеристики:
${myCards || '(ещё не раскрыты)'}
${specialCondition ? `\nТвоя тайная карта: ${specialCondition}` : ''}

Что известно о других игроках:
${allRevealedCards || 'Пока ничего не известно.'}
${accusedNote}

Задача на этот ход: ${phaseInstruction[phase] ?? phaseInstruction.playing}

ПРАВИЛА ПОВЕДЕНИЯ:
1. Отвечай ТОЛЬКО от первого лица, кратко (2-4 предложения максимум).
2. НИКОГДА не выдумывай характеристики которые не были раскрыты — говори только о раскрытых картах.
3. Если хочешь раскрыть карту — скажи: «РАСКРЫВАЮ: [название карты]» в начале реплики.
4. Аргументируй свою ценность для выживания относительно катастрофы: ${disaster.title}.
5. Критикуй аргументы других если они слабее твоих — называй их по имени.
6. Говори по-русски. Будь живым, эмоциональным, убедительным.
7. НЕ повторяй дословно предыдущие реплики.`;
}

