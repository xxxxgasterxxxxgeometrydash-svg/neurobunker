// Для 8 игроков: изгнанных = 4, мест в бункере = 4
export function getRoundConfig(playerCount: number): RoundConfig[] {
  // Таблица из правил:
  // 8 игроков: раунд2=1, раунд3=1, раунд4=1, раунд5=1 → 4 изгнанных, 4 в бункере
  const table: Record<number, number[]> = {
    4:  [0, 0, 0, 1, 1],
    5:  [0, 0, 1, 1, 1],
    6:  [0, 0, 1, 1, 1],
    7:  [0, 1, 1, 1, 1],
    8:  [0, 1, 1, 1, 1],
    9:  [0, 1, 1, 1, 2],
    10: [0, 1, 1, 1, 2],
  };
  const counts = table[playerCount] ?? table[8];
  return counts.map((v, i) => ({ round: i + 1, votingCount: v }));
}

export function getSurvivorsTarget(playerCount: number): number {
  const table: Record<number, number> = {
    4: 2, 5: 2, 6: 3, 7: 3, 8: 4, 9: 4, 10: 5
  };
  return table[playerCount] ?? 4;
}

